package cf.naechelin.vo;

public class LineCommand
{
    public int storeFirst;
    public int storeSecond;
    public int storeThird;
    public int memberId;

    public LineCommand() {}

    public int getStoreFirst()
    {
        return storeFirst;
    }

    public void setStoreFirst(int storeFirst)
    {
        this.storeFirst = storeFirst;
    }

    public int getStoreSecond()
    {
        return storeSecond;
    }

    public void setStoreSecond(int storeSecond)
    {
        this.storeSecond = storeSecond;
    }

    public int getStoreThird()
    {
        return storeThird;
    }

    public void setStoreThird(int storeThird)
    {
        this.storeThird = storeThird;
    }

    public int getMemberId()
    {
        return memberId;
    }

    public void setMemberId(int memberId)
    {
        this.memberId = memberId;
    }
}

